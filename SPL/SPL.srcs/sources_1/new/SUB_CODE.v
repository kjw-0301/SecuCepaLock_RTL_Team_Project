module T_flip_flop(
    input clk,reset_p,
    input t,
    output reg q);
    
    always @(posedge clk or posedge reset_p) begin
        if(reset_p) q=0;
        else  begin
           if(t) q =~q;
           else q = q;
        end
    end

endmodule